#include <cstdio>
#include <memory>
#include <string>

#include "rclcpp/rclcpp.hpp"
#include "rcutils/cmdline_parser.h"

#include "pid/pid.h"

void print_usage()
{
  printf("Usage for controller node:\n");
  printf("controller [-h]\n");
  printf("options:\n");
  printf("-h : Print this help function.\n");
  printf("TODO: link to web docs\n");
}

int main(int argc, char * argv[])
{
  // Force flush of the stdout buffer.
  setvbuf(stdout, NULL, _IONBF, BUFSIZ);

  // Display help?
  if (rcutils_cli_option_exist(argv, argv + argc, "-h")) {
    print_usage();
    return 0;
  }

  // Initialize any global resources needed by the middleware and the client library.
  // You must call this before using any other part of the ROS system.
  // This should be called once per process.
  rclcpp::init(argc, argv);

  // Create the PID node.
  auto my_pid = std::make_shared<pid_ns::PID>();

  // Respond to inputs until shut down
  // TODO: make this rate configurable
  rclcpp::Rate loop_rate(200);
  while (rclcpp::ok())
  {
    rclcpp::spin_some( my_pid );

    my_pid->doCalcs();

    // Add a small sleep to avoid 100% CPU usage
    loop_rate.sleep();
  }

  rclcpp::shutdown();
  return 0;
}
